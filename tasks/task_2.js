// Create variables and assign numeric values
const num1 = 140
const num2 = 13

const addition = num1 + num2 // For Addition
console.log('Addition:', addition) // Output:Addition: 153

const subtraction = num1 - num2 // For Subtraction
console.log('Subtraction:', subtraction) // Output: Subtraction: 127

const multiplication = num1 * num2 // For Multiplication
console.log('Multiplication:', multiplication) // Output: Multiplication: 1820

const division = num1 / num2 // For Division
console.log('Division:', division) // Output: Division: 10.76923076923077

const modulus = num1 % num2 // Modulus (Remainder)
console.log('Modulus:', modulus) // Output: Modulus: 10
